module.exports = {
  name: "autoreaction",
  description: "Auto react to messages containing emojis",
  type: "misc",
  async execute(m, { conn }) {
    try {
      const isAutoReactOn = process.env.AUTOREACTION === "true";
      if (!isAutoReactOn) return;

      if (!m.text || !m.chat || m.fromMe) return;

      const matchedEmoji = [...m.text].find(char => emojis.has(char));
      if (matchedEmoji) {
        await conn.sendMessage(m.chat, {
          react: {
            text: matchedEmoji,
            key: m.key || {},
          }
        });
      }
    } catch (err) {
      console.error("AutoReaction Error:", err);
    }
  }
};
